def _can_print_unicode() -> bool:
    """Check if terminal supports unicode output."""
    try:
        encoding = sys.stdout.encoding or "ascii"
        return encoding.lower() in ("utf-8", "utf8", "utf_8")
    except Exception:
        return False


def print_banner():
    console = Console(force_terminal=True)
    banner = BANNER_UNICODE if _can_print_unicode() else BANNER_ASCII
